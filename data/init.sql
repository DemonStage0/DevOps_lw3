CREATE TABLE IF NOT EXISTS glass (
    id SERIAL PRIMARY KEY,
    "RI" FLOAT NOT NULL,
    "Na" FLOAT NOT NULL,
    "Mg" FLOAT NOT NULL,
    "Al" FLOAT NOT NULL,
    "Si" FLOAT NOT NULL,
    "K" FLOAT NOT NULL,
    "Ca" FLOAT NOT NULL,
    "Ba" FLOAT NOT NULL,
    "Fe" FLOAT NOT NULL,
    "Type" INTEGER NOT NULL
);

CREATE TABLE IF NOT EXISTS predict (
    id SERIAL PRIMARY KEY,
    predicted_class INTEGER NOT NULL,
    "RI" FLOAT NOT NULL,
    "Na" FLOAT NOT NULL,
    "Mg" FLOAT NOT NULL,
    "Al" FLOAT NOT NULL,
    "Si" FLOAT NOT NULL,
    "K" FLOAT NOT NULL,
    "Ca" FLOAT NOT NULL,
    "Ba" FLOAT NOT NULL,
    "Fe" FLOAT NOT NULL,
    timestamp TIMESTAMPTZ DEFAULT NOW()
);